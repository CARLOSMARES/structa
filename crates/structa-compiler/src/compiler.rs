fn compile_dto(node: &AstNode) -> String {
    let name = node.name.as_deref().unwrap_or("Dto");
    let mut out = format!("export class {} {{\n", name);
    out.push_str("    constructor(data) {\n");

    for item in &node.body {
        if item.kind.as_str() == "field" {
            if let Some(field_name) = &item.name {
                out.push_str(&format!(
                    "        this.{} = data.{};\n",
                    field_name, field_name
                ));
            }
        }
    }

    out.push_str("    }\n}\n");
    out
}

fn compile_repository(node: &AstNode) -> String {
    let name = node.name.as_deref().unwrap_or("Repository");
    let mut out = format!("export class {} {{\n", name);

    for item in &node.body {
        if item.kind.as_str() == "method" {
            if let Some(method_name) = &item.name {
                let params = item.props.get("params").map(|s| s.as_str()).unwrap_or("");
                let body = item.props.get("body").map(|s| s.as_str()).unwrap_or("");
                out.push_str(&format!(
                    "    async {}({}) {{\n        {}\n    }}\n",
                    method_name, params, body
                ));
            }
        }
    }

    out.push_str("}\n");
    out
}

fn compile_resolver(node: &AstNode) -> (String, Vec<String>) {
    let name = node.name.as_deref().unwrap_or("Resolver");
    let mut out = format!("export class {} {{\n", name);
    let mut routes = Vec::new();
    let mut pending_decorators: Vec<(String, String)> = Vec::new();

    let mut i = 0;
    while i < node.body.len() {
        let item = &node.body[i];
        match item.kind.as_str() {
            "method" => {
                if let Some(method_name) = &item.name {
                    let params = item.props.get("params").map(|s| s.as_str()).unwrap_or("");
                    let body = item.props.get("body").map(|s| s.as_str()).unwrap_or("");
                    out.push_str(&format!(
                        "    {}({}) {{\n        {};\n    }}\n",
                        method_name, params, body
                    ));

                    for (http_method, path) in pending_decorators.drain(..) {
                        routes.push(format!(
                            "server.route({{ method: '{}', path: '{}', handler: (req, res) => new {}().{}(req, res) }});",
                            http_method, path, name, method_name
                        ));
                    }
                }
            }
            "decorator" => {
                if let Some(method) = &item.name {
                    let path = item.props.get("path").map(|s| s.as_str()).unwrap_or("/");
                    pending_decorators.push((method.clone(), path.to_string()));
                }
            }
            _ => {}
        }
        i += 1;
    }

    out.push_str("}\n");
    (out, routes)
}

fn compile_middleware(node: &AstNode) -> String {
    let name = node.name.as_deref().unwrap_or("Middleware");
    let mut out = format!("export class {} {{\n", name);

    for item in &node.body {
        if item.kind.as_str() == "method" {
            if let Some(method_name) = &item.name {
                let params = item
                    .props
                    .get("params")
                    .map(|s| s.as_str())
                    .unwrap_or("req, res");
                let body = item.props.get("body").map(|s| s.as_str()).unwrap_or("");
                let body_with_semicolons = body.replace(") ", "); ").replace("} ", "}; ");
                out.push_str(&format!(
                    "    {}({}) {{\n        {};\n    }}\n",
                    method_name, params, body_with_semicolons
                ));
            }
        }
    }

    out.push_str("    handle(req, res, next) {\n");
    out.push_str("        next();\n");
    out.push_str("    }\n");
    out.push_str("}\n");
    out.push_str(&format!("server.use(new {}());\n", name));
    out
}

fn compile_module(node: &AstNode) -> String {
    let name = node.name.as_deref().unwrap_or("Module");
    let mut out = format!("export class {} {{\n", name);

    for item in &node.body {
        if item.kind.as_str() == "property" {
            if let Some(prop_name) = &item.name {
                if let Some(value) = item.props.get("value") {
                    if !value.is_empty() {
                        out.push_str(&format!("    {} = {};\n", prop_name, value));
                    }
                }
            }
        } else if item.kind.as_str() == "method" {
            if let Some(method_name) = &item.name {
                let params = item.props.get("params").map(|s| s.as_str()).unwrap_or("");
                let body = item.props.get("body").map(|s| s.as_str()).unwrap_or("");
                out.push_str(&format!(
                    "    {}({}) {{\n        {}\n    }}\n",
                    method_name, params, body
                ));
            }
        }
    }

    out.push_str("}\n");
    out
}
